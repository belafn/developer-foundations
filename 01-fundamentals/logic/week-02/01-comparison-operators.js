/*
===========================================================================================
   COMPARISON OPERATORS (OPERADORES DE COMPARAÇÃO)
===========================================================================================
*/
// Operadores de comparação são usados para comparar dois valores.
// O resultado de qualquer comparação é sempre um valor boolean:
// true ou false.

// Eles são muito usados em:
// - estruturas condicionais (if / else)
// - loops (while, for)
// - validações de dados

//=========================================================================================
// 01- Igualdade (==)
//=========================================================================================
// Compara apenas o valor, ignorando o tipo de dado.

let num = 10;
let textNum = "10";

console.log(num == textNum); // true

// Não é recomendado, pois o JavaScript converte os tipos automaticamente.

//=========================================================================================
// 02- Igualdade estrita (===)
//=========================================================================================
// Compara o valor e o tipo de dado.
// É a forma recomendada.

console.log(num === textNum); // false
console.log(num === 10);      // true

//=========================================================================================
// 03- Diferença (!=)
//=========================================================================================
// Verifica se os valores são diferentes (ignora o tipo).

console.log(num != textNum); // false
console.log(num != 20);      // true

//=========================================================================================
// 04- Diferença estrita (!==)
//=========================================================================================
// Verifica se o valor OU o tipo são diferentes.
// Forma recomendada.

console.log(num !== textNum); // true
console.log(num !== 10);      // false

//=========================================================================================
// 05- Maior que (>)
//=========================================================================================

let a = 5;
let b = 8;

console.log(a > b); // false
console.log(b > a); // true

//=========================================================================================
// 06- Menor que (<)
//=========================================================================================

console.log(a < b); // true
console.log(b < a); // false

//=========================================================================================
// 07- Maior ou igual (>=)
//=========================================================================================

console.log(a >= 5); // true
console.log(b >= 10); // false

//=========================================================================================
// 08- Menor ou igual (<=)
//=========================================================================================

console.log(a <= 5);  // true
console.log(b <= 6);  // false

//=========================================================================================
// 09- Comparação entre strings
//=========================================================================================
// Strings são comparadas pela ordem alfabética (Unicode).

console.log("apple" < "banana"); // true
console.log("z" > "a");          // true

//=========================================================================================
// 10- Comparação com booleanos
//=========================================================================================

console.log(true === true);   // true
console.log(false !== true);  // true

//=========================================================================================
// 11- Undefined e null
//=========================================================================================

let x;
let y = null;

console.log(x == y);   // true
console.log(x === y);  // false

//=========================================================================================
// CONCLUSÃO
//=========================================================================================
// Operadores de comparação permitem tomar decisões no código.
// Boas práticas:
// - prefira sempre === e !==
// - evite == e !=
// - toda comparação retorna um boolean
